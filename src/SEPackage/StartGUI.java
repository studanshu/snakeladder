    private void LadderGoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LadderGoMouseClicked
        int temp=0,temp2=0,flag=0;
        if(StartLadder.getText().equals("") && EndLadder.getText().equals(""))
        {
            new ErrorGUI().setVisible(true);//System.out.println(StartLadder.getText()+"a" +EndLadder.getText());

        }
        else
        {
            try
            {
                temp=Integer.parseInt(StartLadder.getText());
                temp2=Integer.parseInt(EndLadder.getText());

            }
            catch(Exception e)
            {
                flag=1;
                new ErrorGUI().setVisible(true);
            }
            if(flag==0)
            {
                if(temp<=choice*choice && temp2 <=choice*choice)
                {
                    cust.ladders.add(new Ladder(Integer.parseInt(EndLadder.getText()),Integer.parseInt(StartLadder.getText())));
                    LadderArea.append(StartLadder.getText()+","+EndLadder.getText()+"\n");
                }
                else
                new ErrorGUI().setVisible(true);
            }
            StartLadder.setText(null);
            EndLadder.setText(null);
        }
    }//GEN-LAST:event_LadderGoMouseClicked

