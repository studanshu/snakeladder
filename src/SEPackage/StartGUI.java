class Customize{
    public ArrayList<Snake>snakes=new ArrayList();
    public ArrayList<Ladder>ladders=new ArrayList();
    public int players;
    public int size;
    public int HighScore;
    public boolean isCustomized;
    public Customize(int snake,int ladder,int tablesize,int hs)
    {
        while(snakes.size()!=0)
            snakes.remove(0);
        while(ladders.size()!=0)
            ladders.remove(0);
        players=0;
        size=tablesize;
        HighScore=hs;
        isCustomized=false;
    }
}
