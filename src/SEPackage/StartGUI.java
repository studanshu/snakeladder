        if(cust==null)
        {
            cust=new Customize(5,5,8,-1);
            cust.players=3;
            cust.size = 8;
            cust.snakes.add( new Snake(62,6));
            cust.ladders.add(new Ladder(64,26));
            cust.snakes.add( new Snake(45,32));
            cust.ladders.add(new Ladder(36,5));
            cust.snakes.add( new Snake(52,23));
            cust.ladders.add(new Ladder(31,17));
            cust.snakes.add( new Snake(28,2));
            cust.ladders.add(new Ladder(60,51));
            cust.snakes.add( new Snake(19,12));
            cust.ladders.add(new Ladder(47,18));
        }
        cust.players=3;
        cust.HighScore=getHighScore();
        player=new Player[3];
        player[0] = new Player(0);
        player[0].name=Name1.getText();
        player[1] = new Player(1);
        player[1].name=Name2.getText();
        player[2] = new Player(2);
        player[2].name=Name3.getText();
